        public Server_Skilldata(string line)
        {
            string[] dataLine = line.Split('\t');

            if (dataLine.Length > 4)
                for (int i = 0; i < dataLine.Length; i++)
                {
                    FindAndInitializeVariable(dataLine[i]);
                }
            else
            {
                string incorrectEntry = line;
            }

        }

        private string StripExcessServerText(string startText, string variable, string endText)
        {

            if (variable == null)
                variable = "";

            string returnString = string.Copy(variable);

            if (!returnString.Contains(" "))
            {
                startText = startText.Replace(" ", "");
            }

            if (returnString.Contains("  "))
                returnString = returnString.Replace("  ", " ");

            returnString = returnString.Replace(startText, "");
            if (endText.Length > 0)
                returnString = returnString.Replace(endText, "");

            return returnString;
        }

        private string ConvertToServerText(string startText, string variable, string endText)
        {
            if (string.IsNullOrEmpty(variable))
                return "";
            else
            {
                variable = variable.Replace(" ", "");
                return startText + variable + endText + "\t";
            }
        }

